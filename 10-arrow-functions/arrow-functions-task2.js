/*
Задача 2: 
Напишите стрелочную функцию filterEvenNumbers,
которая принимает массив чисел и возвращает новый массив,
состоящий только из четных чисел.
Например, для входного массива [1, 2, 3, 4, 5, 6]
функция должна вернуть [2, 4, 6].
*/

const filterEvenNumbers = (numbers) => {
    console.log(numbers.filter(item => item % 2 === 0));
}

filterEvenNumbers([1, 2, 3, 4, 5, 6]);