/*
Задача 2: 
Напишите функцию checkEvenOdd, которая принимает одно число в качестве аргумента и выводит:
"Число четное", если число делится на 2 без остатка.
"Число нечетное", если остаток от деления на 2 не равен 0.
Подсказка: Используйте оператор % для проверки остатка от деления.
*/

function checkEvenOdd(number) {
    if (number % 2 == 0) {
        console.log("Число четное");
    } else {
        console.log("Число нечетное");
    }
}

checkEvenOdd(2);
checkEvenOdd(3);